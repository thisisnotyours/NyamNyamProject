package com.suek.nyamnyam;

public class FoodCultureItem {

    String title;
    String file;

    String sub;
    String source;
    String msg;

    public FoodCultureItem() {
    }

    public FoodCultureItem(String title, String file, String sub, String source, String msg) {
        this.title = title;
        this.file = file;
        this.sub = sub;
        this.source = source;
        this.msg = msg;
    }
}
